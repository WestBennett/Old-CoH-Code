<div>I know at this point we all feel like NCSoft just shot our dog, heck even I do, but I would like to keep this thread flame free and constructive. The truth is, there were 2 ways this could have gone down: either NCSoft turned off CoH, or NCSoft itself shut down. Either the case we would loose CoH.<br />
 <br />
With all the information that has been put out there,
5a8
 either on these forums, facebook or other website interviews, it seems the possible reason CoH is getting shut down is because CoH was not as profitable as NCSoft needed it to be. I'm creating this thread as a brainstorming for possible options to increase the profitability of CoH. <br />
 <br />
I know, you might be thinking what power do we have over that? More than you think actually! You have to realise that WE are the source of their case flow. However, NCSoft might not have been willing to try certain options out of fear of scaring off clients. I'm hoping they will read this thread, and see what we, as their customer, are willing to do.<br />
 <br />
<b><font color="red">NEW ACCOUNT CONFIGURATION</font></b><br />
<br />
<font color="white">Alot of people on Facebook and on these forums are suggesting that they increase the cost of subscriptions. Heck, even I would pay an extra 5-10$ a month if it meant it would save this game! </font><br />
 <br />
However, this option could also alienate other players, who can't afford to pay more per month. So by doing this they could end up loosing subs, and scaring off potential new customers. <br />
 <br />
Here's what I suggest: add a new account type.<br />
 <br />
Currently we have (to my knowledge, feel free to correct me if I'm wrong) Free, Premium and VIP. What I'm suggesting would be something like Free, Premium, VIP and Gold. All current account types would be there, so p
5a8
layers would not loose the options they already have. The new account type would cost more to allow NCSoft to charge more money and allow the players who do have the extra cash to choose this option. As for what the different benefits this new gold account would have, I'll leave that up to others to speculate. But the ideas are out there, like maybe a freeform character perserver for that account, Or personal nemesis.<br />
 <br />
<b><font color="red">IN GAME ADDS</font></b><br />
<br />
<font color="white">Adding in game advertisement is something that was mentioned in the past. I don't know why it was not implemented, or if it was and it just flopped, but maybe this needs to be revisited. Maybe reduce the cost for in game adds? a little money is better than no money, right? Or NCSoft can use it for free advertising of their other games.</font><br />
 <br />
I remember the community being up in arms and saying it would break game immersion, that they didnt want to pay for this game if reall world advertisements were going to be included, etc,etc,etc. At this point, I'm sure people would rather have in game adds breaking immersion than not having immersion period!<br />
 <br />
<b><font color="red">REDUCE THE AMOUNT OF SERVERS</font></b><br />
<br />
<font color="black"><font color="white">The monetary impact of this would be minimal, but 2 fold.</font> </font><br />
 <br />
First, having/maintaining a server costs money. I
5a8
t consumes electricity, it needs hardware, it needs room, etc. Currently we have 16 servers. If we reduce the number of servers to 8, we would ideally be reducing the cost of running these by half.<br />
 <br />
Second, community would be modified. In the past, merging servers was a great debate; some people wanted it, others didnt. Again, at this point, I believe we would agree to merge servers, over not having servers to log on to, period. Tho an increase in numbers/players on every server would defenetly look more appealing to new players, so this might help attract new customers.<br />
 <br />
<b><font color="red">PAY FOR CONTENT</font></b><br />
<br />
<font color="white">CoH is known for adding new and free content. Over the years they added 23 free issues, which were awesome and we love you guys for doing so. But now I believe that it's time to have us pay for it, because paid for content is better than no content at all.</font><br />
 <br />
What I would suggest is a model similar to DCUO, when they have download packs of content, and that must be paid for. This would allow people to keep playing the game, even if they pay for the content or not. It would work like CoV did; you buy it, you have access to it, you don't, you can't acces it but can still play CoH.<br />
 <br />
This is all that I had off the top of my head. Feel free to add more ideas.</div>


